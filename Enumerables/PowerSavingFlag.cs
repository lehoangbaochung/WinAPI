namespace WindowsAPI.Enumerables
{
    public enum PowerSavingFlag : byte
    {
        /// <summary>
        /// Power saving function is turned off
        /// </summary>
        Off = 0,

        /// <summary>
        /// The power saving function is turned on to save battery,
        /// save as much energy as possible
        /// </summary>
        On = 1
    }
}
